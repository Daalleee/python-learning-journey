angka1 = int(input('masukkan angka 1: '))
angka2 = int(input('masukkan angka 2: '))
oper = input('masukkan operator: ')
if oper == '+':
    hasil= angka1 + angka2
    print(hasil)
elif oper =='-':
    hasil=angka1 - angka2
    print(hasil)
elif oper == '*':
    hasil = angka1 * angka2
    print(hasil)
elif oper == '/':
    hasil = angka1 / angka2
    print(hasil)