input1 = float(input('masukkan angka: '))
input2 = float(input('masukkan angka: '))

print(f'{input1} + {input2} = {input1 + input2}')
print(f'{input1} - {input2} = {input1 - input2}')
print(f'{input1} / {input2} = {input1 / input2}')
print(f'{input1} * {input2} = {input1 * input2}')